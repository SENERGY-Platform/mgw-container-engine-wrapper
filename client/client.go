package client

import (
	"errors"
	"io"
	"net/http"
)

type Client struct {
	httpClient HttpClient
	baseUrl    string
}

func New(httpClient HttpClient, baseUrl string) *Client {
	return &Client{
		httpClient: httpClient,
		baseUrl:    baseUrl,
	}
}

func (c *Client) execRequest(req *http.Request) ([]byte, error) {
	resp, err := c.httpClient.Do(req)
	if err != nil {
		return nil, err
	}
	defer resp.Body.Close()
	body, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, err
	}
	if resp.StatusCode >= 300 {
		return body, errors.New(resp.Status)
	}
	return body, nil
}
